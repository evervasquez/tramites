package librerias;

public class Tramite_model {
	private String tramite;
	private String codigo;
	private String fecha_inicio;
	private String solicitante;
	private String id_tramite;
	private String tipo;
	private String dni;
	private String ruc;
	private String estado;
	private String numero_folios;
	private String usuario;

	public String getTramite() {
		return tramite;
	}

	public void setTramite(String tramite) {
		this.tramite = tramite;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getFecha_inicio() {
		return fecha_inicio;
	}

	public void setFecha_inicio(String fecha_inicio) {
		this.fecha_inicio = fecha_inicio;
	}

	public String getSolicitante() {
		return solicitante;
	}

	public void setSolicitante(String solicitante) {
		this.solicitante = solicitante;
	}
	
	public String getId_tramite() {
		return id_tramite;
	}

	public void setId_tramite(String id_tramite) {
		this.id_tramite = id_tramite;
	}
	
	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	public String getDni() {
		return dni;
	}

	public void setDni(String dni) {
		this.dni = dni;
	}
	
	public String getRuc() {
		return ruc;
	}

	public void setRuc(String ruc) {
		this.ruc = ruc;
	}
	
	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}
	
	public String getNumero_folios() {
		return numero_folios;
	}

	public void setNumero_folios(String numero_folios) {
		this.numero_folios = numero_folios;
	}
	
	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
}
